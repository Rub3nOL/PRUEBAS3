    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        
        String nombre = txtNombre.getText();
        
        char[] caracteresContrasena = txtContrasena.getPassword();
        
        String contrasena = new String(caracteresContrasena);
        
        if(nombre.isEmpty() || contrasena.isEmpty()){
            lblError.setText("Error: caracteres vacios");
            return;
        }
        
        Usuario usuario = UsuarioDAO.login(nombre, contrasena);
        
        if(usuario == null){
            lblError.setText("Error: usuario y contraseña no coincide");
            
        }
        
        if (usuario != null) {
            dispose();
            if (usuario.getRol() == Roles.ADMINISTRADOR) {
                new AdminPanelFrame(nombre).setVisible(true);
            } else {
                new ProfesorFrame(nombre).setVisible(true);
            }
        }
        
        
    }//GEN-LAST:event_btnEntrarActionPerformed

